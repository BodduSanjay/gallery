class Gallery extends Component {
  state = {
    activeId: imagesList[0].id,
    imageUrl: imagesList[0].imageUrl,
    imageAltText: imagesList[0].imageAltText,
  }

  onClickChangeImg = (id, imageUrl, imageAltText) => {
    this.setState({
      activeId: id,
      imageUrl: imageUrl,
      imageAltText: imageAltText,
    })
  }

  render() {
    const {activeId, imageUrl, imageAltText} = this.state

    return (
      <div className="bg-cont">
        <div className="dv">
          <div className="imgcont">
            <img src={imageUrl} alt={imageAltText} />
          </div>
          <div>
            <h1>Nature Photography</h1>
            <p>Nature Photography by Rahul</p>
          </div>
          <ul>
            {imagesList.map(eachOne => (
              <ThumbnailItem
                eachOne={eachOne}
                key={eachOne.id}
                onClickChangeImg={this.onClickChangeImg}
                isActiveItem={activeId === eachOne.id}
              />
            ))}
          </ul>
        </div>
      </div>
    )
  }
}
export default Gallery
